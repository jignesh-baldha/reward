if [ "${SHOPWARE_ELASTICSEARCH_ENABLED:-false}" = "true" ]; then
  curl -X GET "http://${SHOPWARE_ELASTICSEARCH_HOST:-opensearch}:${SHOPWARE_ELASTICSEARCH_PORT:-9200}/_cat/health?pretty"
fi

